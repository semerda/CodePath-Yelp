    static func getFilterSettings(selections: [String]) -> [AnyObject] {
        
        var deal = false as Bool
        var sort = .bestMatched as YelpSortMode
        var radius = "0" as String
        var categories = [] as [String]
        
        for block in selections {
            
            let blockParts = block.components(separatedBy: ":")
            
            // Deal
            if "0" == blockParts[0] {
                deal = ("1" == blockParts[2]) ? true : false
            }
            
            // Sort
            if "1" == blockParts[0] {
                if "1" == blockParts[2] {
                    sort = YelpSortMode.distance
                } else if "2" == blockParts[2] {
                    sort = YelpSortMode.highestRated
                } else {
                    sort = YelpSortMode.bestMatched
                }
            }
            
            // Distance
            if "2" == blockParts[0] {
                radius = blockParts[2]
            }
            
            // Categories
            if "3" == blockParts[0] {
                categories.append(blockParts[2])
            }
        }
        
        print("deal: \(deal)")
        print("sort: \(sort)")
        print("categories: \(categories)")
        print("radius: \(radius)")
        
        return [deal as AnyObject, sort as AnyObject, radius as AnyObject, categories as AnyObject]
    }
    
    static func getSavedSettings() -> [AnyObject] {
        let defaults = UserDefaults.standard
        print("yelp_filters_raw: \(defaults.object(forKey: "yelp_filters_raw")!)")
        if (defaults.object(forKey: "yelp_filters_raw") != nil) {
            let selections = defaults.object(forKey: "yelp_filters_raw") as! [String]
            return getFilterSettings(selections: selections)
        } else {
            return []
        }
    }
